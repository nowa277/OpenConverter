const VPR_MASK_DIFF = Buffer.from([0x25, 0xDF, 0xE8, 0xA6, 0x75, 0x1E, 0x75, 0x0E, 0x2F, 0x80, 0xF3, 0x2D, 0xB8, 0xB6, 0xE3, 0x11, 0x00]);

function getMask(pos) {
  let offset = pos >> 4;
  let value = 0;
  while (offset >= 0x11) {
    value ^= TABLE1[offset % 272];
    offset >>= 4;
    value ^= TABLE2[offset % 272];
    offset >>= 4;
  }
  return MASK_V2_PRE_DEF[pos % 272] ^ value;
}

function buildFakeKgm(plaintext, key17, isVpr) {
  // KuGou encrypt is the INVERSE of the huangbao/MyKgmWasm decrypt formula.
  //
  // Decrypt (huangbao/MyKgmWasm):
  //   D(c) = f(c ^ k) ^ f(msk) ^ (VPR_MASK_DIFF if VPR)
  //   where f(w) = w ^ ((w & 0x0f) << 4)  [nibble swap, self-inverse]
  //
  // We want E(audio) such that D(E(audio)) = audio:
  //   f(E(audio) ^ k) ^ f(msk) ^ VPR = audio
  //   f(E(audio) ^ k) = audio ^ f(msk) ^ VPR
  //   E(audio) ^ k = f(audio ^ f(msk) ^ VPR)
  //   E(audio) = f(audio ^ f(msk) ^ VPR) ^ k
  const headerLen = 0x2C + 16;
  const header = Buffer.alloc(headerLen);
  (isVpr ? VPR_HEADER : KGM_HEADER).copy(header, 0);
  header.writeUInt32LE(headerLen, 0x10);
  for (let i = 0; i < 16; i++) header[0x1C + i] = key17[i];

  const cipher = Buffer.alloc(plaintext.length);
  for (let i = 0; i < plaintext.length; i++) {
    const msk = getMask(i);
    const fMsk = msk ^ ((msk & 0x0f) << 4);
    let a = plaintext[i] ^ fMsk;
    if (isVpr) a ^= VPR_MASK_DIFF[i % 17];
    const a2 = a ^ ((a & 0x0f) << 4); // f(a)
    cipher[i] = a2 ^ key17[i % 17];
  }
  return Buffer.concat([header, cipher]);
}

function test() {
  let failed = 0;

  // --- Test 1: magic + size validation ---
  console.log('Test 1: magic + size validation...');
  try {
    decryptBuffer(Buffer.alloc(32));
    console.log('  FAIL: should throw for too-small');
    failed++;
  } catch (e) {
    if (e.message.includes('too small')) console.log('  ✓ PASS (rejected < 0x2C bytes)');
    else { console.log('  FAIL: wrong error:', e.message); failed++; }
  }
  try {
    const bad = Buffer.alloc(0x40);
    decryptBuffer(bad);
    console.log('  FAIL: should throw for bad magic');
    failed++;
  } catch (e) {
    if (e.message.includes('bad magic')) console.log('  ✓ PASS (rejected bad magic)');
    else { console.log('  FAIL: wrong error:', e.message); failed++; }
  }

  // --- Test 2: round-trip KGM ---
  console.log('Test 2: round-trip KGM with synthetic plaintext...');
  const keyKgm = Buffer.from([0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x00]);
  const plaintext = Buffer.from('ID3\x04\x00\x00\x00\x00\x00\x00' + 'fake MP3 frame for kgm test. '.repeat(100), 'utf8');
  const fakeKgm = buildFakeKgm(plaintext, keyKgm, false);
  const decrypted = decryptBuffer(fakeKgm);
  if (decrypted.equals(plaintext)) {
    console.log(`  ✓ PASS (${plaintext.length} bytes round-tripped)`);
  } else {
    failed++;
    let firstDiff = -1;
    for (let i = 0; i < plaintext.length; i++) {
      if (decrypted[i] !== plaintext[i]) { firstDiff = i; break; }
    }
    console.log(`  FAIL: first diff at byte ${firstDiff}: got 0x${decrypted[firstDiff]?.toString(16)} expected 0x${plaintext[firstDiff]?.toString(16)}`);
  }

  // --- Test 3: round-trip VPR (with VPR_MASK_DIFF applied) ---
  console.log('Test 3: round-trip VPR with synthetic plaintext...');
  const keyVpr = Buffer.from([0x10, 0x20, 0x30, 0x40, 0x50, 0x60, 0x70, 0x80, 0x90, 0xa0, 0xb0, 0xc0, 0xd0, 0xe0, 0xf0, 0x42, 0x00]);
  const ptVpr = Buffer.from('fLaC' + '\x00'.repeat(20) + 'fake FLAC frame for vpr test. '.repeat(100), 'utf8');
  const fakeVpr = buildFakeKgm(ptVpr, keyVpr, true);
  const decVpr = decryptBuffer(fakeVpr);
  if (decVpr.equals(ptVpr)) {
    console.log(`  ✓ PASS (${ptVpr.length} bytes round-tripped)`);
  } else {
    failed++;
    let firstDiff = -1;
    for (let i = 0; i < ptVpr.length; i++) {
      if (decVpr[i] !== ptVpr[i]) { firstDiff = i; break; }
    }
    console.log(`  FAIL: first diff at byte ${firstDiff}: got 0x${decVpr[firstDiff]?.toString(16)} expected 0x${ptVpr[firstDiff]?.toString(16)}`);
  }

  // --- Test 4: format detection ---
  console.log('Test 4: format detection...');
  const cases = [
    { audio: Buffer.from('ID3' + '\x00'.repeat(20)), expected: 'mp3' },
    { audio: Buffer.from('fLaC' + '\x00'.repeat(20)), expected: 'flac' },
    { audio: Buffer.from('OggS' + '\x00'.repeat(20)), expected: 'ogg' },
    { audio: Buffer.from([0xff, 0xfb, 0x90, 0x44]), expected: 'mp3' },
    { audio: Buffer.concat([Buffer.alloc(4), Buffer.from('ftypM4A ')]), expected: 'm4a' },
  ];
  let fmtOk = true;
  for (const c of cases) {
    if (inferFormat(c.audio) !== c.expected) {
      fmtOk = false;
      console.log(`    FAIL: ${c.audio.slice(0, 8).toString('hex')} → got ${inferFormat(c.audio)}, expected ${c.expected}`);
    }
  }
  if (fmtOk) console.log('  ✓ PASS (5/5)'); else failed++;

  // --- Test 5: round-trip on real MP3 via ffmpeg + decodeFile ---
  console.log('Test 5: round-trip on real MP3 → fake .kgm → MP3...');
  const mp3Path = path.join(OUT_DIR, 'sample.mp3');
  if (!fs.existsSync(mp3Path)) {
    try {
      execFileSync('ffmpeg', [
        '-y', '-f', 'lavfi', '-i', 'sine=frequency=440:duration=2',
        '-ac', '1', '-ar', '22050', '-b:a', '64k', mp3Path,
      ], { stdio: 'pipe' });
    } catch (e) { console.log('  SKIP (ffmpeg not available)'); }
  }
  if (fs.existsSync(mp3Path)) {
    const original = fs.readFileSync(mp3Path);
    const realKey = Buffer.from([0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff, 0xa5, 0x00]);
    const fake = buildFakeKgm(original, realKey, false);
    const fakePath = path.join(OUT_DIR, 'sample.kgm');
    fs.writeFileSync(fakePath, fake);
    // Move original out of the way so decodeFile doesn't overwrite it
    const backup = path.join(OUT_DIR, 'sample.original.mp3');
    fs.copyFileSync(mp3Path, backup);

    const { outputPath, format } = decodeFile(fakePath, OUT_DIR);
    const recovered = fs.readFileSync(outputPath);
    if (format === 'mp3' && recovered.equals(original)) {
      let dur = -1;
      try {
        dur = parseFloat(execFileSync('ffprobe', [
          '-v', 'error', '-show_entries', 'format=duration',
          '-of', 'default=noprint_wrappers=1:nokey=1', outputPath,
        ], { stdio: ['pipe', 'pipe', 'pipe'] }).toString().trim());
      } catch {}
      if (dur > 1.5) console.log(`  ✓ PASS (${original.length} bytes, ffprobe duration=${dur.toFixed(2)}s)`);
      else { console.log(`  FAIL: ffprobe duration=${dur}`); failed++; }
    } else {
      console.log(`  FAIL: format=${format}, recovered equals original=${recovered.equals(original)}`);
      failed++;
    }
  }

  if (failed > 0) {
    console.log(`\n${failed} test(s) failed.`);
    process.exit(1);
  }
  console.log('\nAll KGM tests passed.');
}

test();
