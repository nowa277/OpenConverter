// A real KGM/VPR file has a 16-byte magic header. We build one with a
// synthetic plaintext, encrypt using the same algorithm, decrypt, verify.
function encrypt(plaintext, key17, isVpr) {
  const header = Buffer.from(isVpr ? VPR_HEADER : KGM_HEADER);
  // Use a header length larger than the header itself so audio starts after
  const headerLen = 0x2C + 16; // 0x2C (header bytes) + 16 bytes padding
  const out = Buffer.alloc(headerLen);
  header.copy(out, 0);
  out.writeUInt32LE(headerLen, 0x10);
  // Copy key (16 bytes) into header at offset 0x1C
  for (let i = 0; i < 16; i++) out[0x1C + i] = key17[i];
  // Pad the rest with zeros (already zero)
  const encrypted = Buffer.alloc(plaintext.length);
  // Inline copy of the decrypt function (since encrypt is the inverse)
  for (let i = 0; i < plaintext.length; i++) {
    let med8 = key17[i % 17] ^ plaintext[i];
    med8 ^= (med8 & 0x0f) << 4;
    // getMask(i)
    let offset = i >> 4;
    let value = 0;
    const { getMask } = require('../src/decoders/kgm');
    // Reuse getMask from kgm.js — but it's not exported. Re-implement locally:
    while (offset >= 0x11) {
      // TABLES not exported either, so use the actual decrypt's getMask.
      // To keep the test self-contained, we encrypt by re-running decrypt
      // backwards: apply same XOR. Actually encrypt == decrypt for XOR.
    }
  }
  // The encrypt and decrypt operations are identical (all XOR and nibble swap,
  // which is its own inverse). So just apply the same transformation.
  return null; // not used; see buildFakeKgm below
}

