/**
 * Generate synthetic KGM / KGMA / VPR test vectors for the Android port.
 *
 * The KGM cipher is involutory (nibble-swap is self-inverse, all ops are XOR),
 * so encrypt == decrypt. We use the real desktop decoder to compute the
 * expected plaintext sha256, then re-encrypt to produce the synthetic .kgm /
 * .kgma / .vpr files. The Kotlin decoder must reproduce the exact same bytes.
 *
 * KGM and KGMA share the same KGM magic (the desktop decoder doesn't
 * distinguish them — only the file extension differs). VPR uses a different
 * 16-byte magic and applies VPR_MASK_DIFF in the cipher.
 *
 * Outputs:
 *   - synthetic-kgm.kgm   (KGM magic, encrypted MP3, expected → "mp3")
 *   - synthetic-kgma.kgma (KGM magic, encrypted FLAC, expected → "flac")
 *   - synthetic-vpr.vpr   (VPR magic, encrypted MP3, expected → "mp3")
 *   - expected-sha256.json (sha256 of each *decrypted* audio, plus kgm_key
 *                           and vpr_key for documentation)
 *
 * The 17-byte keys (last byte is implicitly 0) are embedded in the synthetic
 * headers at offset 0x1C. The Kotlin decoder must read them from there, not
 * hard-code them — this is the byte-level TDD contract.
 */
