const mp3Path = path.join(OUT_DIR, 'sample.mp3');
genAudio('mp3', 'libmp3lame', mp3Path);
const mp3Bytes = fs.readFileSync(mp3Path);
const kgmCipher = buildFakeKgm(mp3Bytes, KGM_KEY, false);
const kgmOut = path.join(ANDROID_RES_DIR, 'synthetic-kgm.kgm');
fs.writeFileSync(kgmOut, kgmCipher);
const kgmDec = decryptBuffer(kgmCipher);
if (!kgmDec.equals(mp3Bytes)) throw new Error('KGM round-trip mismatch');
const kgmSha = crypto.createHash('sha256').update(mp3Bytes).digest('hex');
console.log(`  ${kgmOut} (${kgmCipher.length} bytes) — plaintext sha256=${kgmSha}`);

